#import "UIColor+Withings.h"

@implementation UIColor (Withings)

+ (UIColor*)withingsBlueColor
{
    return [UIColor colorWithRed:13/255.0 green:181/255.0 blue:1.0 alpha:1.0];
}

+ (UIColor*)withingsLightBlueColor
{
    return [UIColor colorWithRed:164/255.0 green:226/255.0 blue:1.0 alpha:1.0];
}

@end
